describe("validateRoutineSpec — rejects malformed specs with clear errors", () => {
	test("step missing required `id` produces a required-keyword error pointing at the offending step", () => {
		const malformed = {
			name: "missing-id",
			trigger: [{ cron: "0 7 * * *" }],
			steps: [
				// missing `id` field
				{ type: "wait", duration_secs: 1 },
			],
		};
		const result = validateRoutineSpec(malformed);
		expect(result.valid).toBe(false);
		expect(result.errors).toBeDefined();
		// At least one error should point at /steps/0 and complain about missing `id`
		const hasIdError = result.errors!.some(
			(e) =>
				e.keyword === "required" &&
				(e.params as { missingProperty?: string }).missingProperty === "id" &&
				e.path.startsWith("/steps/0"),
		);
		expect(hasIdError).toBe(true);
	});

	test("step with unknown discriminator `type` fails the oneOf", () => {
		const malformed = {
			name: "bad-type",
			trigger: [{ cron: "0 7 * * *" }],
			steps: [{ id: "bogus", type: "frobnicate", payload: "irrelevant" }],
		};
		const result = validateRoutineSpec(malformed);
		expect(result.valid).toBe(false);
		expect(result.errors).toBeDefined();
		// oneOf failure produces an error at /steps/0 with keyword="oneOf"
		// (Ajv also emits per-branch sub-errors at /steps/0/allOf/<n>/properties/type/const).
		const hasOneOfError = result.errors!.some(
			(e) => e.keyword === "oneOf" && e.path.startsWith("/steps/0"),
		);
		expect(hasOneOfError).toBe(true);
	});

	test("webhook trigger missing `secret_env` produces required-keyword error", () => {
		const malformed = {
			name: "bad-webhook",
			trigger: [
				{ webhook: { path: "/hooks/x" } }, // missing secret_env
			],
			steps: [{ id: "noop", type: "wait", duration_secs: 1 }],
		};
		const result = validateRoutineSpec(malformed);
		expect(result.valid).toBe(false);
		expect(result.errors).toBeDefined();
		const hasSecretEnvError = result.errors!.some(
			(e) =>
				e.keyword === "required" &&
				(e.params as { missingProperty?: string }).missingProperty === "secret_env",
		);
		expect(hasSecretEnvError).toBe(true);
	});
});
